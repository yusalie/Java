package practice;
public class MathOperators {
    public static void main(String[] args) {
        int bagOfsweets = 10;
        int bagOfSweets2 = 20;

        long starsInMilkWay = 2500000000L;
        long starsInAndromeda = 1000000000000L;

        double testAvg = 55.6;
        double testAvg2 = 77.4;
        // addition
        System.out.println(bagOfsweets+bagOfSweets2);
        System.out.println(starsInAndromeda+starsInMilkWay);
        System.out.println(testAvg+testAvg2);
        //subtraction
        System.out.println(bagOfsweets-bagOfSweets2);
        System.out.println(starsInMilkWay-starsInAndromeda);
        System.out.println(testAvg-testAvg2);
        //multiplication
        System.out.println(bagOfsweets*bagOfSweets2);
        System.out.println(starsInMilkWay*starsInAndromeda);
        System.out.println(testAvg*testAvg2);
        //division
        System.out.println(bagOfsweets/bagOfSweets2);
        System.out.println(starsInMilkWay/starsInAndromeda);
        System.out.println(testAvg/testAvg2);
    }
}